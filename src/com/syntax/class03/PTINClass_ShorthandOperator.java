package com.syntax.class03;

public class PTINClass_ShorthandOperator {

	public static void main(String[] args) {

		int num=100;
		num+=500;
		num+=200;
		System.out.println(num);
		
		num-=60;
		System.out.println(num);
		
		int cakePiece=11;
		cakePiece/=4;
		
		int cake=25;
		cake%=7;
		
		System.out.println(cakePiece);
		System.out.println(cake);
		
				
		
	}

}
