package com.syntax.homework;

public class HWClass07_Loop_01 {

	public static void main(String[] args) {
// Print number from 1 to 100 in 1 line with space

		for (int a = 1; a <= 100; a++) {
			System.out.print(a + " ");
		}
// Print number from 100 to 1
		System.out.println();
		for (int b = 100; b >= 1; b--) {
			System.out.println(b);
		}
	}
}
