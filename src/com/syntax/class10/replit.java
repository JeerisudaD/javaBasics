package com.syntax.class10;

public class replit {

	public static void main(String[] args) {

		int x = 0;
		for (System.out.println("1"); x < 5; System.out.println("3")) {
			System.out.println(" Hello");
			x++;
		}
		System.out.println("--------------");

		int y = 0;
		for (System.out.println("1" + y); x < 5; System.out.println("3" + y)) {
			System.out.println(" Hello");
			y++;
		}

		System.out.println("--------------");

		
		
	}

}
